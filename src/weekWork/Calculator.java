package weekWork;

public class Calculator {
	
	double a;
	
	double b;
	
	double result;
	
	
	
	double addition(double a, double b) {
		
		result = a + b;
		
		return result;
	}
	
	double subtraction(double a, double b) {
		
		result = a - b;
		
		return result;
	}
	
	double multiplication(double a, double b) {
		
		result = a * b;
		
		return result;
	}
	
	double division(double a, double b) {
		
		result = a / b;
		
		return result;
	}
}
